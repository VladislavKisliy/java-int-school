package com.wdt.java;

public class GenWord {
	
	private Integer id;
	private String word;
	private String desc;
	
	public Integer getId(){
		return this.id;
	}	
	
	public void setId(Integer id){
		this.id = id;
	}
	
	public String getWord(){
		return this.word;
	}	
	
	public void setWord(String word){
		this.word = word;
	}	
	
	public String getDesc(){
		return this.desc;
	}	
	
	public void setDesc(String desc){
		this.desc = desc;
	}		
}
